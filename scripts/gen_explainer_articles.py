def gen_related_html(art):
    """관련 글 카드 HTML"""
    items = []
    for url, badge, title in art["related"]:
        items.append(f'        <a href="{url}" class="related-card"><span class="related-badge">{badge}</span><span class="related-title">{title}</span></a>')
    return "\n".join(items)


def gen_faq_jsonld(art):
    """FAQPage JSON-LD"""
    import re as _re
    items = []
    for q, a in art["faq"]:
        # strip HTML tags from answer for JSON-LD
        clean_a = _re.sub(r'<[^>]+>', '', a).replace('"', "'")
        clean_q = q.replace('"', "'")
        items.append(f'      {{"@type": "Question", "name": "{clean_q}", "acceptedAnswer": {{"@type": "Answer", "text": "{clean_a}"}}}}')
    return ",\n".join(items)


